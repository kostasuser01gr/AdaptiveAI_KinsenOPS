/** Lookup map for fast key-based access */
export const CONFIG_MAP = new Map(CONFIG_REGISTRY.map((d) => [d.key, d]));

/** Get the default value for a config key */
export function getConfigDefault(key: string): unknown {
  return CONFIG_MAP.get(key)?.defaultValue;
}
