/**
 * System Configuration Registry
 * Defines every configurable value in the platform with metadata,
 * types, defaults, validation rules, and categorisation.
 */

export interface ConfigDefinition {
  key: string;
  category: ConfigCategory;
  label: string;
  description: string;
  type: "string" | "number" | "boolean" | "json" | "string[]";
  defaultValue: unknown;
  validation?: {
    min?: number;
    max?: number;
    pattern?: string;
    options?: string[];
  };
  sensitive?: boolean;
  restartRequired?: boolean;
}

export type ConfigCategory =
  | "storage"
  | "integrations"
  | "automations"
  | "media"
  | "rate_limits"
  | "operational"
  | "websocket"
  | "security"
  | "ai"
  | "connections"
  | "channels";

